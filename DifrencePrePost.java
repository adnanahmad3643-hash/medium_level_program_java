public class DifrencePrePost {
    // 18.	Difference between pre-increment and post-increment
    public static void main(String[] args) {
        int a=10;
        System.out.println(a++);// postIncremenent increase the value after the Execution..
        System.out.println(++a);//PreIncremenent Increase the value between the Execution..
    }
}
